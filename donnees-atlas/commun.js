/* ── Résolveur de noms historiques de villes ── */
function resolveCityName(city, year) {
  if (city.noms) {
    const m = city.noms.find(n => year >= n.debut && year < n.fin);
    if (m) return { nom: m.nom, local: m.local || null };
  }
  return { nom: city.nom_actuel || "?", local: null };
}

