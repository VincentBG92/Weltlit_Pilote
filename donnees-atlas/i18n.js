let LANG = 'fr';

function setLang(lang) {
  LANG = lang;
  document.dispatchEvent(new CustomEvent('langchange', {detail: lang}));
}

function localName(nameEn) {
  if (!nameEn || LANG === 'en') return nameEn;
  const t = TERRITORY_NAMES[nameEn];
  if (!t) return nameEn;
  return t[LANG] || nameEn;
}

